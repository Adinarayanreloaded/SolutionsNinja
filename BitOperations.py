def bitwise_operations(a, b):
    # Calculate the bitwise left shift of a by 1 position
    left_shift_result = a << 1
    
    # Calculate the bitwise right shift of b by 1 position
    right_shift_result = b >> 1
    
    # Print the results
    print(left_shift_result)
    print(right_shift_result)

# Example usage
bitwise_operations(12, 7)
